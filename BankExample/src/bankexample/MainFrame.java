    private void updateCustomerDisplay(Customer customer) {
        this.accounts = this.bank.getAccounts(customer);
        this.accountCount.setText("Total Accounts: " + this.accounts.size());
        updateAccountDisplay();
        this.firstNameLabel.setText(customer.getFirstName());
        this.lastNameLabel.setText(customer.getLastName());
        this.emailLabel.setText(customer.getEmail());
    }
