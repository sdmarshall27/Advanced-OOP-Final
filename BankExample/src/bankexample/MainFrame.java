    private void updateAccountDisplay() {
        Account account = this.accounts.get(accountIndex);                
        this.accountTypeLabel.setText(account.getAccountType().toString() 
                + " " + accountIndex);
        this.accountBalanceLabel.setText("" + account.getBalance());
    }
